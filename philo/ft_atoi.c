long long int	ft_atoi(const char *str)
{
	long long int	r;
	int				s;

	r = 0;
	s = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '-' || *str == '+')
		if (*str++ == '-')
			s *= -1;
	while (*str >= '0' && *str <= '9' && *str)
		r = r * 10 + (*str++ - 48);
	return (r * s);
}
